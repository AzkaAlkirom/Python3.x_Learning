# Belajar Loop (Perulangan)



# Ini looping pakai list
print("\n"+" Loop Pake List ".center(32,"="))

angka2_list = [0,2,4,6,8,10] # ini list nya 

for i in angka2_list: #ini loop nya
    print(f"Ini angka {i}")


# ini looping pakai range

print("\n"+" Loop Pake Range ".center(32,"="))
angka2_range = range(1,8) #ini range 1-5

for i in angka2_range: #ini loop nya
    print(f"Ini angka perulangan ke --> {i}")
    # print("Aku Kuat")


# ini looping with a string 

print("\n"+" Loop Pake string ".center(32,"="))
angka2_range = "Semangat Bro" #ini string nya

for i in angka2_range: #ini loop nya
    print(i) #outputnya akan menurun

